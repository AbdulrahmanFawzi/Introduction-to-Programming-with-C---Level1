#include <iostream>
using namespace std;

int main(){

    int a, b ;

    cout<<"Please enter the first number A:\n";
    cin>> a;

    cout<<"Please enter the first number B:\n";
    cin>> b;

    cout<< a << " + " << b << " =" << a + b << endl;
    cout<< a << " - " << b << " = "<< a - b << endl;
    cout<< a << " * " << b << " = "<< a * b << endl;
    cout<< a << " / " << b << " = "<< a / b << endl;
    cout<< a << " % " << b << " = "<< a % b << endl;

    return 0 ;
}